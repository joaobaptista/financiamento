import { createI18n } from 'vue-i18n';

export function normalizeLocaleForApp(locale) {
    const raw = String(locale || '').trim();
    if (!raw) return 'pt_BR';

    const normalized = raw.replace('-', '_');

    if (normalized.toLowerCase() === 'pt_br') return 'pt_BR';
    if (normalized.toLowerCase().startsWith('pt_')) return 'pt_BR';

    if (normalized.toLowerCase().startsWith('en')) return 'en';
    if (normalized.toLowerCase().startsWith('es')) return 'es';

    if (normalized.toLowerCase() === 'pt') return 'pt_BR';

    return normalized;
}

